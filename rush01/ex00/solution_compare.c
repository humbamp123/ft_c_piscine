int			solution_compare(int **sol1, int **sol2)
{
	int i;
	int j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (sol1[i][j] != sol2[i][j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
