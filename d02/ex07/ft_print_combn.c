void		ft_print_combn(void)
{
	int i;

	while (i != 789)
	{
		i++;
		if ( i / 100 % 10 < i / 10 % 10 && i / 10 % 10 < i % 10 )
		{
			ft_putchar(i / 100 % 10 + '0');
			ft_putchar(i / 10 % 10 + '0');
			ft_putchar(i % 10 + '0');
			if (i != 789)
				ft_putchar(',');
			if (i != 789)
				ft_putchar(' ');
		}
	}
}
